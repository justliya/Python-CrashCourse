#for loops outputs each stored value in a variable 
toppings = ['Pepporoni', 'Sausage', 'Plain']
for topping in toppings:
    print('I like ' + topping.lower() + ' pizza!')
    print('I love pizza so much!\n')
    
animals = ['cheetah','jaguar','cougar']
for animal in animals:
    print('A ' + animal + ' is very fast!')
print('These animals are very fast!\n')
    